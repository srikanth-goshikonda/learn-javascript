//JavaScript Bitwise Operators
let a =5 //Binary -> 101
let b =6 //Binary -> 110

//Bitwise AND
console.log(a & b) //4

//Bitwise OR
console.log(a | b) //7

//Bitwise XOR
console.log(a ^ b) //3

//Bitwise NOT
console.log( ~b) //2's complement -> -7

//Bitwise LEFT SHIFT
console.log(a << b) //320

//Bitwise RIGHT SHIFT
console.log(a >> b) //0

//Bitwise Zero-fill right shift
console.log(a >>> b) //0


